void	ft_diese_to_alpha(char **tetri, int k)
{
	int i;
	int j;

	i = 0;
	while (tetri[i])
	{
		j = 0;
		while (tetri[i][j])
		{
			if (tetri[i][j] == '#')
				tetri[i][j] = tetri[i][j] + 30 + k;
			j++;
		}
		i++;
	}
}
